#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*head;
	t_list	*node;
	void	*tmp;

	if (!lst || !f)
		return (NULL);
	head = NULL;
	while (lst)
	{
		tmp = (*f)(lst->content);
		node = ft_lstnew(tmp);
		if (!node)
		{
			if (tmp)
				free(tmp);
			ft_lstclear(&head, del);
			return (NULL);
		}
		ft_lstadd_back(&head, node);
		lst = lst->next;
	}
	return (head);
}
